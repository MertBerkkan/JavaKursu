package bolum.uc.calisma;

public class MathMetodlari {

	public static void main(String[] args) {

		System.out.println("---------------CEİL---------");
		System.out.println(Math.ceil(3.1));
		System.out.println(Math.ceil(3.8));
		System.out.println(Math.ceil(3.7));
		System.out.println(Math.ceil(3.4));
		System.out.println(Math.ceil(3.499999));
		
		
		
		System.out.println("---------------FLOOR---------");
		System.out.println(Math.floor(3.1));
		System.out.println(Math.floor(3.8));
		System.out.println(Math.floor(3.7));
		System.out.println(Math.floor(3.4));
		System.out.println(Math.floor(3.499999));
		
		System.out.println("---------------RİNT---------");
		System.out.println(Math.rint(3.1));
		System.out.println(Math.rint(3.8));
		System.out.println(Math.rint(3.7));
		System.out.println(Math.rint(3.4));
		System.out.println(Math.rint(3.499999));
		
		
		System.out.println("---------------ROUND---------");
		System.out.println(Math.round(3.1));
		System.out.println(Math.round(3.8));
		System.out.println(Math.round(3.7));
		System.out.println(Math.round(3.4));
		System.out.println(Math.round(3.499999));
	}

}
